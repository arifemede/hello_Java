package day07loop;

public class C07Review {

    public static void main(String[] args) {


        //Example 1: Type code to print integers from 4 to 7



        for(int i=4; i<8; i++ ){

            System.out.print( i + " ");

        }

        System.out.println(" ");


        System.out.println("While loop: ");

        int i =4;

        while(i<8){

            System.out.print(i + " ");

            i++;

        }

    }
}
